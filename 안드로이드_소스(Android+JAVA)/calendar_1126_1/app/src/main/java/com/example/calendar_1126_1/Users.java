package com.example.calendar_1126_1;

public class Users {
    private String ID;
    private String PW;
    private String NAME;
    private String TEL;

    public String getID() {
        return ID;
    }

    public void setID(String id) {
        ID = id;
    }

    public String getPW() {
        return PW;
    }

    public void setPW(String pw) {
        PW = pw;
    }

    public String getNAME() {
        return NAME;
    }

    public void setNAME(String NAME) {
        this.NAME = NAME;
    }

    public String getTEL() {
        return TEL;
    }

    public void setTEL(String TEL) {
        this.TEL = TEL;
    }
}